# draw square in Python Turtle
import turtle

t = turtle.Turtle()



# drawing first side
t.forward(100) 
t.left(90) 

# drawing second side
t.forward(100) 
t.left(90) 

# drawing third side
t.forward(100) 
t.left(90) 

# drawing fourth side
t.forward(100) # Forward turtle by s units
t.left(90) # Turn turtle by 90 degree
